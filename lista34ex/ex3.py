#Faça um script que leia duas notas de um aluno e mostre como resultado sua média.
print(f'Coloque os valores a seguir para fazer sua média!!')
nota1 = float(input("Digite a sua primeira nota aqui:"))
nota2 = float(input("Digite a sua segunda nota aqui:"))

print(f'Sua média final é: {(nota1 + nota2) // 2}')